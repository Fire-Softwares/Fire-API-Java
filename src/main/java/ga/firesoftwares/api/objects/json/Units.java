package ga.firesoftwares.api.objects.json;

public class Units {
	
	// Fields
	private String disk;
	private String ram;
	
	// Getters and setters
	public String getDisk() {
		return disk;
	}
	public void setDisk(String disk) {
		this.disk = disk;
	}
	public String getRam() {
		return ram;
	}
	public void setRam(String ram) {
		this.ram = ram;
	}
	
}
