package ga.firesoftwares.api.enums;

/**
 * The algorithm of encryption used.
 * @author Renaud
 *
 */
public enum EncryptionAlgorithms {
	/**
	 * Message Digest 5 encryption algorithm.
	 */
	MD5,
	/**
	 * Secure Hash Algorithm encryption algorithm.
	 */
	SHA1,
	/**
	 * 256 bits Secure Hash Algorithm encryption algorithm.
	 */
	SHA256,
	/**
	 * 384 bits Secure Hash Algorithm encryption algorithm.
	 */
	SHA384,
	/**
	 * 512 bits Secure Hash Algorithm encryption algorithm.
	 */
	SHA512;
}
